// In JavaScript, an expression is a combination of values, variables, operators, and functions that can be evaluated to produce a result. It is any valid unit of code that resolves to a value. Expressions can represent simple or complex operations, and they are an essential part of writing dynamic and functional code in JavaScript.

// Example:

// 1. Literal Values
5; // Numeric literal
("Hello, World!"); // String literal
true; // Boolean literal

// 2. Variables
var x = 10;
var y = x * 2;

// 3. Operators
var sum = x + y;
var product = x * y;

//4. Function Calls
function square(num) {
  return num * num;
}

var result = square(4);

//5 . Array and Object Initializers:
var numbers = [1, 2, 3, 4, 5];
var person = { name: "John", age: 30 };

// 6. Complex Expression
var complexExpression = x * y + 2 * x - y / 2;

//Summaries:  JavaScript expressions are a fundamental part of building logic and performing computations in your code. They are used in assignments, function arguments, conditions, loops, and more. Understanding and using expressions effectively is crucial for writing functional and dynamic JavaScript programs.
