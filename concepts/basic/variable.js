// In JavaScript, variables are used to store and manage data. They are named containers that can hold values of various types, such as numbers, strings, or objects. Variables in JavaScript are declared using the var, let, or const keyword, followed by a unique name (identifier).

// 1. var
// Variable declaration using var
var age;

// Variable assignment
age = 25;

// Variable declaration and assignment in one line
var name = "John";

// Reassigning a variable
age = 30;

// Output the values
console.log(name); // Output: John
console.log(age); // Output: 30

// 2. let
// Variable declaration using let
let score;

// Variable assignment
score = 100;

// Variable declaration and assignment in one line
let playerName = "Alice";

// Reassigning a variable
score = 150;

// Output the values
console.log(playerName); // Output: Alice
console.log(score); // Output: 150

// 3. const

// Constant declaration and assignment
const pi = 3.14;

// Constants must be assigned a value when declared
// const gravity; // This would result in an error

// Output the value
console.log(pi); // Output: 3.14

// Note:
// const is also block-scoped, but unlike let, you cannot reassign values to a variable declared with const once it's been assigned.
